#include "iostream"
#include "stdlib.h"
#include "chrono"
#include "thread"

void printSky(char skyF[10][10], char skyB[10][10]){
    for(int b=0;b<10;b++){
        for(int g=0;g<10;g++){
            std::cout << skyF[b][g] << " ";
        }
        std::cout << "\n";
    }
    std::cout.flush();
    for(int b=10;b>0;b--){
        std::cout << "\033[F";
        for(int g=10;g>0;g--){
            std::cout << "\r";
            std::cout << skyB[b][g] << " ";
        }
    }
}

